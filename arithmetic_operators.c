#include<stdio.h>

int main()
{
	//initilizing of a number
	int a,b;
	
	//declaration of number
	a = 52;
	b = 3;
	
	//arithmetic operators basic
	printf("a+b: %d\n", a+b);
	printf("a-b: %d\n", a-b);		
	printf("a*b: %d\n", a*b);
	printf("a/b: %d\n", a/b);
	printf("a%b: %d\n", a%b);
	return 0;
}
